    private void btnExecuteActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnExecuteActionPerformed
    {//GEN-HEADEREND:event_btnExecuteActionPerformed
        final Generator generator;
        final DBGeneratorDefinition dbGenDefinition;
        final ProgressUpdateObserver observer = this;
        
        cancel = false;
        RandomDefinitionsBuilder builder = new RandomDefinitionsBuilder();
        builder.setFilename( txtFilename.getText() );
        builder.setSAXHandler( new DBGenDefSAXHandler() );
        Vector vData = builder.getElements();
        
        dbGenDefinition = (DBGeneratorDefinition) vData.get(0);
        generator = new Generator();                       
        
        SwingWorker worker = new SwingWorker()
        {
            public Object construct()
            {
                generator.setDBGeneratorDefinition(dbGenDefinition);
                generator.registerObserver(observer);
                generator.generate();
                return null;
            }
        };
        worker.start();
    }//GEN-LAST:event_btnExecuteActionPerformed
    
