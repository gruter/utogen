    public boolean dataGenProgressContinue(String msg, int progress)
    {
        final int v=progress;
        final String s = msg;
        final JProgressBar progBar = progressBar;
        final JTextArea area = txtMessages;
        final String m = msg;
        
        SwingUtilities.invokeLater( new Runnable()
        {
            public void run()
            {
                 progBar.setValue(v);
                 //frmProgress.setLabelMessage(s);
                 area.append(m + ", " + v + "\n");
            }
        });                        
        
        return cancel;
    }
    
